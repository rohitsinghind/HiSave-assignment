clicked = []

function imgFunc (){
images.filter((e)=>{
    return e.id == display.filter((x)=>{
        return e.id == x
    })
}).map((e)=>{
   return document.getElementById('container').innerHTML += "<div id="+e.element+" class='grid-item'>"+
"<img class='image' src="+e.url+"' alt=''>"+
"</div>"
})
}

imgFunc();

function clickHandle (e){
    clicked.push(e);
    var highest = Math.max(...display)
    if(highest < images.length)
    {
        for(var i=0; i<display.length ; i++){
            if(display[i]==e){
                display[i]==highest+1;
            }
        }
    }
    document.getElementById('container').innerHTML = ""
    imgFunc();
}

